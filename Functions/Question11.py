# Question 11: Write a Python program to create a lambda function that adds 15 to a given
# number passed in as an argument, also create a lambda function that multiplies
# argument x with argument y and print the result.

# lambda function that adds 15 to a given number
add_15 = lambda a: a + 15
print(add_15(30))

# lambda function that multiplies argument x with argument y
mul = lambda a, b: a * b
print(mul(2, 3))